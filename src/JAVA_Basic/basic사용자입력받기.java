package JAVA_Basic;

public class basic사용자입력받기 {

    public static void main(String[] args){
        int[] abc = {1,2,3,4,5};
        System.out.println("매개변수의 개수:"+abc.length);
        for(int i = 0 ; i<abc.length; i++){
            System.out.println("abc[" + i + "] = \""+ abc[i] + "\"");


        }
    }
}
